    class ClientThread extends Thread {
        public void run() {
            while(true) {
                try {
                    String dataMsg = gnClient.NhanDuLieu();
                    
                    if (dataMsg.startsWith("NV")) {
                        String[] lines = dataMsg.split("\n");
                        for (String line : lines) {
                            String[] parts = line.split("[|]");
                            Object[] t = new Object[3];
                            t[0] = parts[0].replace("NV:", "");
                            t[1] = parts[1];
                            t[2] = parts[2];
                            mht.addRow(t);
                        }

                        jTable1.setModel(mht);
                    }
                    
                    if (dataMsg.equals("null")) {
                        JOptionPane.showMessageDialog(null, "Nhap lai ma don vi");
                    }
                    
                } catch (Exception ex) {
                    Logger.getLogger(frmServer.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }
