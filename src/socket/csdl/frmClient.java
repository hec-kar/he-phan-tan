    private void txtMsgKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtMsgKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == 10) {
            try {
                String host = txtIpAddress.getText();
                int port = Integer.parseInt(txtPort.getText());
                String ipAddress = InetAddress.getLocalHost().getHostAddress();
                String msg = ipAddress + ":" + txtMsg.getText();
                gnClient.GuiDuLieu(msg, host, port);
                txtMsg.setText("");
                mht.setRowCount(0);
            } catch (Exception e) {
                e.printStackTrace();
            }
        }

    }//GEN-LAST:event_txtMsgKeyPressed

